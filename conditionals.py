red_apples = 33
green_apples = 72
yellow_apples = 100

if red_apples > yellow_apples:
    print('There are less red apples than green apples')
elif yellow_apples < green_apples:
    print ('There are more yellow apples than green apples')
else: 
    print('We need more apples')

